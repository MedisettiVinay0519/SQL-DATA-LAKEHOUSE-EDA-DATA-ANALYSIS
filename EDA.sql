--Find the date of the first and last order
SELECT MIN(order_date)  as first_order_date from gold.fact_sales;

SELECT MAX(order_date) as last_order_date from gold.fact_sales;

--HOW MANY YEARS OF SALES ARE AVAIABLE
SELECT DATEDIFF(YEAR,MIN(order_date),MAX(order_date)) as Order_range_years
from gold.fact_sales;

--HOW MANY MONTH OF SALES ARE AVAIABLE
SELECT DATEDIFF(MONTH,MIN(order_date),MAX(order_date)) as Order_range_months
from gold.fact_sales

--FIND YOUNGEST AND OLDEST CUSTOMER
SELECT MIN(birthdate) as oldest_customer,DATEDIFF(year,MIN(birthdate),getdate()) as oldest_customer_age,
       MAX(birthdate) as youngest_customer,DATEDIFF(year,MAX(birthdate),getdate()) as newest_customer_age
from gold.dim_customers;

