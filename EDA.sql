--which 5 products generate the highest revenue?
SELECT *
FROM (
SELECT p.product_name,SUM(f.sales_amount) as total_sales,
        RANK() OVER( order by Sum(f.sales_amount) DESC) as rnk
FROM gold.fact_sales as f
LEFT JOIN gold.dim_products as p
on p.product_key=f.product_key 
Group by p.product_name
)t
where rnk<=5 ;

--which 5 products generate the lowest revenue?
SELECT *
FROM (
SELECT p.product_name,SUM(f.sales_amount) as total_sales,
        RANK() OVER( order by Sum(f.sales_amount) asc) as rnk
FROM gold.fact_sales as f
LEFT JOIN gold.dim_products as p
on p.product_key=f.product_key 
Group by p.product_name
)t
where rnk<=5 ;

--FIND THE TOP 10 CUSTOMERS WITH HIGHEST REVENUE
SELECT top 10
c.customer_key,c.first_name,c.last_name ,SUM(f.sales_amount) as total_sales
FROM gold.fact_sales as f
LEFT JOIN gold.dim_customers as c
on c.customer_key=f.customer_key 
Group by c.customer_key,c.first_name,c.last_name
ORDER BY total_sales DESC;

--FIND THE TOP 3 CUSTOMERS WITH FEW ORDERS
SELECT top 3
c.customer_key,c.first_name,c.last_name,COUNT( DISTINCT f.order_number) as total_orders
FROM gold.fact_sales as f
LEFT JOIN gold.dim_customers as c
on c.customer_key=f.customer_key 
Group by c.customer_key,c.first_name,c.last_name
ORDER BY total_orders ASC;
