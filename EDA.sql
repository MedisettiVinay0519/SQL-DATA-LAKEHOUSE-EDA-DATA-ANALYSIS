SELECT dc.country,
        SUM(fs.quantity) as TOTAL_QUANTITY,
        SUM(fs.sales_amount) as TOTAL_SALES_AMOUNT 
FROM gold.fact_sales as fs 
JOIN  gold.dim_customers as dc 
